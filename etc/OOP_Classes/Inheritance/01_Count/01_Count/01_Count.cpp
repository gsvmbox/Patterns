// конструкторы в производных классах
#include <iostream>

using namespace std;
///////////////////////////////////////////////////////////
class Counter
{
  protected:                         // не следует использовать private
    unsigned int count;              // счетчик
  public:
    Counter ( ) : count ( )          // конструктор без параметров
      { }
    Counter ( int c ) : count ( c )  // конструктор с одним параметром
      { }
    unsigned int get_count ( ) const // получение значения
      { return count; }
    Counter operator++ ( )           // оператор увеличения
      { return Counter ( ++count ); }
};
///////////////////////////////////////////////////////////
class CountDown : public Counter
{
  public:
    CountDown ( ) : Counter ( )        // конструктор без параметров
      { }
    CountDown ( int c ) : Counter ( c )// конструктор с одним параметром
      { }
    CountDown operator-- ( )           // оператор уменьшения
      { return CountDown ( --count ); }
};
///////////////////////////////////////////////////////////
int main ( )
{
  CountDown c1;                            // переменные класса CountDn
  CountDown c2 ( 100 );

  cout << "\nc1 = " << c1.get_count ( ); // выводим значения на экран
  cout << "\nc2 = " << c2.get_count ( );

  ++c1; ++c1; ++c1;                      // увеличиваем c1
  cout << "\nc1 = " << c1.get_count ( ); // показываем результат

  --c2; --c2;                            // уменьшаем c2
  cout << " c2 = " << c2.get_count ( );   // показываем результат

  CountDown c3 = --c2;                     // создаем переменную c3 на основе c2
  cout << "\nc3 = " << c3.get_count ( ); // показываем значение

  cout << endl;


  return 0;
}

